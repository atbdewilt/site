    <h2>NS App</h2>

    <p>Hier vind je meer informatie over de opdracht, aan welke eisen het moet voldoen en hoe je het moet gaan opleveren. Deze opdracht wordt gebruik om een beeld te krijgen van jouw kennis omtrent PHP. We vinden het belangrijk dat je vooral kan uitleggen wat je hebt gedaan, niet dat er een perfecte kant-en-klare applicatie staat. Zorg dus dat je begrijpt wat je doet zodat je bij een volgend gesprek jouw keuzes kan onderbouwen.</p>

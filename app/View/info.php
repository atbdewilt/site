    <h3>Briefing</h3>
    <p>Bouw een kleine versie van de 9292 app als webapplicatie, maak hierbij gebruik van de NS API voor actuele data: <a href="http://apiportal.ns.nl/">http://apiportal.ns.nl/</a></p>

    <h3>Tijdsinschatting:</h3>
    <ul>
        <li>Noteer hoeveel uur jij denkt nodig te hebben;</li>
        <li>Wat verwacht jij dat jij in deze tijd kan opleveren.</li>
    </ul>

    <h3>Minimale eisen:</h3>
    <ul>
        <li>Toon een lijst met treinen die aankomen en vertrekken van een station die gekozen kan worden doormiddel van een zoek functie;</li>
        <li>Zorg dat vanuit het gekozen station een route gepland kan worden naar minimaal één aander station met daarbij de aankomsttijd en prijs van de rit;</li>
        <li>Maak gebruik van Composer;</li>
        <li>Push de code naar een GitHub repository (of andere publieke git service);</li>
        <li>Zorg dat de applicatie makkelijk te testen is, denk hierbij mogelijk aan een docker container;</li>
        <li>Er mag geen gebruik worden gemaakt van een framework (bijv. Laravel of Codeigniter), er mogen wel losse PHP packages worden gebruik.</li>
    </ul>

